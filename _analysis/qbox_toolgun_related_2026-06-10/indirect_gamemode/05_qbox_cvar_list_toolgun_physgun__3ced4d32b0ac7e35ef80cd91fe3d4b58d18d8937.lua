local replicated = {} -- debug

local CreateClientConVar=CreateClientConVar or function(...) ErrorNoHalt"CreateClientConVar no exist?\n" end

function _G.CreateClientConVar(cvar,origval,save,toserver,...)
	
	local critical = needinstant[cvar]
	if critical then
		return CreateClientConVar(cvar,origval,save,toserver,...)
	end
	
	
	local ret = CreateClientConVar(cvar,origval,save,false,...)
	if toserver then
		if not replicated[cvar] then
			replicated[cvar] = true
			
			cvars.AddChangeCallback(cvar,replicator,Tag)
			local val = ret:GetString()
			send_queue[cvar] = val
			replicated_value[cvar]=val
		end
		
	end
	return ret
end


concommand.Add('ccvar_dump',function()
	local t = {}
	local isrep = {}
	for cvar,_ in next,replicated do
		t[#t+1] = cvar
		isrep[cvar] = stringtable[cvar] and true or false
	end
	
	table.sort(t)
	
	local str = table.concat(t,'\n')
	for _,v in next,t do
		print((isrep[v] and "<X> " or "< > ")..v)
	end
	
	file.Write("ccvar.txt",str)
	print("\nWritten to ccvar.txt!\n")
end)

end -- MAIN


