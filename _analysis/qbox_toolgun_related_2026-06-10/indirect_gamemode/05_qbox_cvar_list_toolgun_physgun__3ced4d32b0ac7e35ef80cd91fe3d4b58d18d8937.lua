local Tag="networkcvar"

local function MAIN(stringtable)

local ccvar_debug = file.Exists("cfg/cccvdebug.flag.cfg","MOD")
function engine.setCCCVDebug(set)
	ccvar_debug = set
end
local function dbg(...)
	if ccvar_debug then
		Msg"[PlayerConVar CCCV] " print(...)
	end
end
if SERVER then
	AddCSLuaFile()
	local Player = FindMetaTable"Player"
	util.AddNetworkString(Tag)
	local netvars=setmetatable({},{__mode='k',
		__index=function(self,key)
			local t={}
			self[key]=t
			return t
		end})
	_G.clientnetvars = netvars
	net.Receive(Tag,function(len,pl)
		local t=netvars[pl]
		local isinstringtable = net.ReadBit()==1
		
		local cvar
		if isinstringtable then
			local id = net.ReadUInt(12)
			cvar=stringtable[id]
			assert(cvar,"!?!?")
		else
			cvar = net.ReadString()
		end
		
		local new = net.ReadString()
		local old = t[cvar]
		t[cvar] = new
		
		hook.Run("PlayerConVarChanged", pl, cvar, old, new)
		
		dbg(pl,cvar,new)
		
	end)

	local Player_GetInfo = Player.GetInfo

	function Player:GetInfo(cvar_name)
		local t = netvars[self]
		local val = t[cvar_name]
		if val ~= nil then return val end

		return Player_GetInfo(self, cvar_name)
	end

	local Player_GetInfoNum = Player.GetInfoNum

	function Player:GetInfoNum(cvar_name, fallback_value)
		local t = netvars[self]
		local val = t[cvar_name]
		if val ~= nil then return tonumber(val) or fallback_value end

		return Player_GetInfoNum(self, cvar_name, fallback_value)
	end

	return
	
end

-- use writebit maybe?
local function sender(cvar,val)
	net.Start(Tag)
		
		local keyid=stringtable[cvar]
		net.WriteBit(keyid and 1 or 0)
		if keyid then
			net.WriteUInt(keyid,12)
		else
			net.WriteString(cvar)
		end
		
		net.WriteString(tostring(val))
		
	net.SendToServer()
end

local send_queue=util.GetCCvarQueue and util.GetCCvarQueue() or {}

function util.GetCCvarQueue()
	return send_queue
end

local replicated_value = {}

local function replicator(cvar, old, new)

	if old == new or replicated_value[cvar] == new then
		
		-- Allow first update no matter what
		if replicated_value[cvar] then return end
	end
	dbg(cvar, "->", new)
	
	replicated_value[cvar] = new

	send_queue[cvar] = new
end

local first = true
--Tick only gets called on usercmd
hook.Add("Tick",Tag,function()
	--TODO: combine in netmessages instead of here for size optimization
	for _=1,3 do
		local cvar,val = next(send_queue)
		
		if cvar==nil then
			if first then
				first = nil
				Msg"[CCVar] "print"Finished sending"
			end
			return
		end
		
		send_queue[cvar] = nil
		sender(cvar,val)
		
	end
	
end)
hook.Add("HUDPaint",Tag,function()
	if not first then return end
	surface.SetFont("ChatFont")
	surface.SetTextPos(32,128)
	surface.SetTextColor(Color(255,255,255,200))
	surface.DrawText(("CCCV replication: %d remaining"):format(table.Count(send_queue)))
end)

